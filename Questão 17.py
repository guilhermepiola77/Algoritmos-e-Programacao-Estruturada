# 17. Leia quatro números e calcule a média aritmética e imprima o resultado.

numero1 = float(input("Informe o primeiro número: "))
numero2 = float(input("Informe o segundo número: "))
numero3 = float(input("Informe o terceiro número: "))
numero4 = float(input("Informe o quarto número: "))

media = (numero1 + numero2 + numero3 + numero4) / 4

print("A média dos quatros números é:",media)
