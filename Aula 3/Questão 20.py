# 20. Leia um número inteiro e imprima o seu antecessor e o seu sucessor.

numero_inteiro = int(input("Digite um numero inteiro: "))

antecessor = numero_inteiro -1
sucessor = numero_inteiro + 1

print("O antecessor do numero inteiro é: ",antecessor)
print("O sucessor do numero inteiro é: ",sucessor)
