# Leia uma temperatura em graus Celsius e apresente-a convertida em graus Kelvin. A
# fórmula de conversão é: 𝐾 = 𝐶 + 273.15, sendo 𝐶 a temperatura em Celsius e 𝐾 a
# temperatura em Kelvin.

Celsius = float(input("Informe a temperatura em Kelvin:"))
Kelvin = (Celsius + 273,15)
print("A temperatura em Kelvin é:",Kelvin)
